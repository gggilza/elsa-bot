def register_group(message: types.Message):
    chat = message.chat
    cid  = str(chat.id)
    now  = datetime.now().strftime("%d.%m.%Y %H:%M:%S")
    if cid not in GROUPS:
        GROUPS[cid] = {
            "title":       chat.title or "Личка",
            "type":        chat.type,
            "first_seen":  now,
            "last_activity": now,
        }
    else:
        GROUPS[cid]["title"]         = chat.title or "Личка"
        GROUPS[cid]["last_activity"] = now
    save_groups()

